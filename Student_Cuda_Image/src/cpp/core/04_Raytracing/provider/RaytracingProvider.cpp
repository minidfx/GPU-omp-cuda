RayTracing* RayTracingProvider::create()
    {
    int dw = 500;
    int dh = 500;

    float dt = 60.0f / 1000.0f;
    int nSphere = 100;

    return new RayTracing(dw, dh, nSphere, dt, 3, "CUDA RayTracing");
    }

Image* RayTracingProvider::createGL(void)
    {
    return new Image(create());
    }

